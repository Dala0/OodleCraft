	state.YNEG = YNEG
	state.XPOS = XPOS
	state.XNEG = XNEG
	state.ZPOS = ZPOS
	state.ZNEG = ZNEG
	state.draw_face = draw_face
	state.draw_cube = draw_cube
	state.draw_XNEG = draw_ZNEG
	state.draw_XPOS = draw_ZPOS
	state.draw_YNEG = draw_ZNEG
