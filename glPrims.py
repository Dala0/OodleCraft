def draw_face(face, andColour = True, N=o):
	if face == ZNEG:
		if andColour: glColor3f(0.75,0.75,0.75)
		glNormal3f(0,0,-1)
		glTexCoord2f( 0, 0)
		glVertex3f( -o, -o, -N)
		glTexCoord2f( 1, 0)
		glVertex3f(  o, -o, -N)
		glTexCoord2f( 1, 1)
		glVertex3f(  o,  o, -N)
		glTexCoord2f( 0, 1)
		glVertex3f( -o,  o, -N)
	if face == ZPOS:
		if andColour: glColor3f(0.8,0.8,0.8)
		glNormal3f(0,0,1)
		glTexCoord2f( 0, 0)
		glVertex3f( -o, -o,  N)
		glTexCoord2f( 1, 0)
		glVertex3f(  o, -o,  N)
		glTexCoord2f( 1, 1)
		glVertex3f(  o,  o,  N)
		glTexCoord2f( 0, 1)
		glVertex3f( -o,  o,  N)
	if face == YNEG:
		if andColour: glColor3f(0.5,0.5,0.5)
		glNormal3f(0,-1,0) 
		glTexCoord2f( 0, 0)
		glVertex3f( -o, -N, -o)
		glTexCoord2f( 1, 0)
		glVertex3f(  o, -N, -o)
		glTexCoord2f( 1, 1)
		glVertex3f(  o, -N,  o)
		glTexCoord2f( 0, 1)
		glVertex3f( -o, -N,  o)
	if face == YPOS:
		if andColour: glColor3f(1.0,1.0,1.0)
		glNormal3f(0,1,0) 
		glTexCoord2f( 0, 0)
		glVertex3f( -o,  N, -o)
		glTexCoord2f( 1, 0)
		glVertex3f(  o,  N, -o)
		glTexCoord2f( 1, 1)
		glVertex3f(  o,  N,  o)
		glTexCoord2f( 0, 1)
		glVertex3f( -o,  N,  o)
	if face == XNEG:
		if andColour: glColor3f(0.85,0.85,0.85)
		glNormal3f(-1,0,0)
		glTexCoord2f( 1, 0)
		glVertex3f( -N, -o, -o)
		glTexCoord2f( 1, 1)
		glVertex3f( -N,  o, -o)
		glTexCoord2f( 0, 1)
		glVertex3f( -N,  o,  o)
		glTexCoord2f( 0, 0)
		glVertex3f( -N, -o,  o)                      
	if face == XPOS:
		if andColour: glColor3f(0.9,0.9,0.9)
		glNormal3f(1,0,0)
		glTexCoord2f( 1, 0)
		glVertex3f(  N, -o, -o)
		glTexCoord2f( 1, 1)
		glVertex3f(  N,  o, -o)
		glTexCoord2f( 0, 1)
		glVertex3f(  N,  o,  o)
		glTexCoord2f( 0, 0)
		glVertex3f(  N, -o,  o)

